void main() {
  //Các phép tính cơ bản
  int tongSo = 2 + 2;
  int hieuSo = 2 - 2;
  int tichSo = 2 * 2;
  double thuongSo = 2 / 2;

  print(tongSo);
  print(hieuSo);
  print(tichSo);
  print(thuongSo);

  int layPhanDu = 7 % 2;
  print(layPhanDu);

  //Phép chia lấy phần nguyên
  int layPhanNguyen = 7 ~/ 2;
  print(layPhanNguyen);

  //Phép gán và tính toán kết hợp
  int x = 10;
  x += 5;
  x -= 2;
  x *= 2;
  x ~/= 2;
  print(x);
}
